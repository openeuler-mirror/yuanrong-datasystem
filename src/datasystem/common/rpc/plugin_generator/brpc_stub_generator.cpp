std::string RpcGenerator::BuildAsyncReadImpl(const google::protobuf::MethodDescriptor &method)
{
    // AsyncRead: look up tag, wait for RPC completion via cv, copy response, clean up.
    // If flags contains DONTWAIT, returns K_TRY_AGAIN if the RPC hasn't completed yet.
    std::string impl =
        "::datasystem::Status $stub$::$methodName$AsyncRead(int64_t tagId, $outputTypeName$ &reply"
        "$optRecvPayload1$,\n"
        "    ::datasystem::RpcRecvFlags flags) {\n"
        "    // For DONTWAIT: peek first without removing from map.\n"
        "    if (flags == ::datasystem::RpcRecvFlags::DONTWAIT) {\n"
        "        auto call = asyncCtx_.GetCall(tagId);\n"
        "        if (call == nullptr) {\n"
        "            return ::datasystem::Status(::datasystem::StatusCode::K_INVALID,\n"
        "                __LINE__, __FILE__,\n"
        "                \"Tag \" + std::to_string(tagId) + \" not found\");\n"
        "        }\n"
        "        std::lock_guard<std::mutex> lock(call->mtx);\n"
        "        if (!call->completed) {\n"
        "            return ::datasystem::Status(::datasystem::StatusCode::K_TRY_AGAIN,\n"
        "                __LINE__, __FILE__, \"Async RPC not yet completed\");\n"
        "        }\n"
        "    }\n"
        "    // Remove from map. After this the tag is consumed regardless of outcome.\n"
        "    auto call = asyncCtx_.TakeCall(tagId);\n"
        "    if (call == nullptr) {\n"
        "        return ::datasystem::Status(::datasystem::StatusCode::K_INVALID,\n"
        "            __LINE__, __FILE__,\n"
        "            \"Tag \" + std::to_string(tagId) + \" not found\");\n"
        "    }\n"
        "    // Transfer response ownership from call->response unique_ptr.\n"
        "    auto respGuard = std::move(call->response);\n"
        "    auto *typedResp = static_cast<$outputTypeName$*>(respGuard.get());\n"
        "    {\n"
        "        std::unique_lock<std::mutex> lock(call->mtx);\n"
        "        call->cv.wait(lock, [&call] { return call->completed; });\n"
        "    }\n"
        "    if (call->failed) {\n"
        "        ::datasystem::Status embedded = ::datasystem::TryExtractStatusFromResponse(*typedResp);\n"
        "        if (embedded.IsError()) {\n"
        "            return embedded;\n"
        "        }\n"
        "        return ::datasystem::TryExtractStatusFromControllerError(call->errorText);\n"
        "    }\n"
        "    reply.CopyFrom(*typedResp);\n";
    if (HasPayloadRecvOption(method)) {
        impl += BuildAsyncRecvPayloadFramingSnippet();
    }
    impl +=
        "    return ::datasystem::Status::OK();\n"
        "}\n";
    return impl;
}

std::string RpcGenerator::BuildAsyncRecvPayloadFramingSnippet()
{
    return
        "    // Deserialize recv payload from response attachment (same wire format as sync).\n"
        "    // Uses responseAttachment (snapshot taken in OnRpcDone under lock) instead of\n"
        "    // cntl.response_attachment() to avoid lock-free access after Done returns.\n"
        "    butil::IOBuf &rspBuf = call->responseAttachment;\n"
        "    recvBuffer.clear();\n"
        "    do {\n"
        "        if (rspBuf.empty()) { break; }\n"
        "        int64_t count = 0;\n"
        "        if (rspBuf.copy_to(reinterpret_cast<void*>(&count),\n"
        "            sizeof(count)) != sizeof(count)) { break; }\n"
        "        rspBuf.pop_front(sizeof(count));\n"
        "        recvBuffer.resize(static_cast<size_t>(count));\n"
        "        for (auto& msg : recvBuffer) {\n"
        "            int64_t sz = 0;\n"
        "            if (rspBuf.copy_to(reinterpret_cast<void*>(&sz),\n"
        "                sizeof(sz)) != sizeof(sz)) { break; }\n"
        "            rspBuf.pop_front(sizeof(sz));\n"
        "            if (msg.Resize(static_cast<size_t>(sz)).IsError()) { break; }\n"
        "            if (rspBuf.copy_to(msg.Data(), static_cast<size_t>(sz))\n"
        "                != static_cast<size_t>(sz)) { break; }\n"
        "            rspBuf.pop_front(static_cast<size_t>(sz));\n"
        "        }\n"
        "    } while (false);\n";
}

