// Brpc client stub header
// ============================================================================

void RpcGenerator::CreateBrpcStubHeader(const google::protobuf::FileDescriptor &file,
                                        compiler::GeneratorContext *generatorCtx) const
{
    std::unique_ptr<io::ZeroCopyOutputStream> outputFile(generatorCtx->Open(fileName + ".brpc.stub.pb.h"));
    io::Printer printer(outputFile.get(), '$');

    GenerateBrpcStubPrologue(printer, file);
    printer.PrintRaw(namespaceBegin);

    for (auto i = 0; i < file.service_count(); ++i) {
        auto *svc = file.service(i);
        GenerateBrpcStubClass(printer, *svc, PREFIX);
        GenerateBrpcGenericStubClass(printer, *svc, PREFIX);
    }

    printer.PrintRaw(namespaceEnd);
    printer.PrintRaw(ENDIF);
}

void RpcGenerator::GenerateBrpcStubPrologue(io::Printer &printer,
                                            const google::protobuf::FileDescriptor &file) const
{
    std::map<std::string, std::string> vars;
    vars["full_file_name"] = file.name();
    vars["file_name"] = fileName;
    std::string fileNameHeader = fileName;
    std::replace(fileNameHeader.begin(), fileNameHeader.end(), '/', '_');
    vars["file_name_header"] = fileNameHeader;
    std::string impl =
        "// Generated by the protocol buffer compiler.  DO NOT EDIT!\n"
        "// source: $full_file_name$\n"
        "#ifndef DATASYSTEM_PROTO_BRPC_$file_name_header$_STUB_H\n"
        "#define DATASYSTEM_PROTO_BRPC_$file_name_header$_STUB_H\n"
        "#include <memory>\n"
        "#include <string>\n"
        "#include <vector>\n";
    if (file.message_type_count() > 0) {
        impl += "#include \"$file_name$.pb.h\"\n";
    }
    impl +=
        "#include <brpc/channel.h>\n"
        "#include <google/protobuf/descriptor.h>\n"
        "#include \"datasystem/common/rpc/brpc_async_context.h\"\n"
        "#include \"datasystem/common/rpc/brpc_client_unary_writer_reader.h\"\n"
        "#include \"datasystem/common/rpc/brpc_client_stream_writer_reader.h\"\n"
        "#include \"datasystem/common/rpc/brpc_client_stream_impl.h\"\n"
        "#include \"datasystem/common/rpc/brpc_status_util.h\"\n"
        "#include \"datasystem/common/rpc/rpc_message.h\"\n"
        "#include \"datasystem/common/rpc/rpc_options.h\"\n"
        "#include \"datasystem/common/rpc/rpc_stub_base.h\"\n"
        "#include \"datasystem/common/rpc/rpc_unary_client_impl.h\"\n"
        "#include \"datasystem/utils/status.h\"\n";
    printer.Print(vars, impl.c_str());
    for (auto k = 0; k < file.dependency_count(); ++k) {
        auto depend = file.dependency(k)->name();
        bool isAProto = StripSuffix(depend, ".proto");
        if (isAProto) {
            vars["depend"] = depend;
            printer.Print(vars, "#include \"$depend$.pb.h\"\n");
        }
    }
}

void RpcGenerator::GenerateBrpcStubClass(io::Printer &printer, const google::protobuf::ServiceDescriptor &svc,
                                         const std::string &indent) const
{
    (void)indent;
    const std::string &svcName = svc.name();
    std::map<std::string, std::string> vars;
    vars["svc_name_string"] = "\"" + svcName + "\"";
    vars["stub"] = svcName + "_BrpcStub";
    vars["full_svc_name"] = packageName.empty() ? svcName : packageName + "." + svcName;

    const std::string constructor =
        "class $stub$ {\n"
        "public:\n"
        "    explicit $stub$(brpc::Channel *channel, int32_t timeoutMs = -1)\n"
        "        : channel_(channel), timeoutMs_(timeoutMs)\n"
        "        , svcDesc_(::google::protobuf::DescriptorPool::generated_pool()\n"
        "              ->FindServiceByName(\"$full_svc_name$\")) {}\n"
        "    ~$stub$() = default;\n"
        "    static std::string FullServiceName() { return \"$full_svc_name$\"; }\n"
        "    static std::string ServiceName() { return $svc_name_string$; }\n"
        "    ::datasystem::Status GetInitStatus() const {\n"
        "        if (channel_ == nullptr) {\n"
        "            return ::datasystem::Status(::datasystem::StatusCode::K_NOT_READY,\n"
        "                                        __LINE__, __FILE__, \"brpc channel is null\");\n"
        "        }\n"
        "        if (svcDesc_ == nullptr) {\n"
        "            return ::datasystem::Status(::datasystem::StatusCode::K_NOT_READY,\n"
        "                                        __LINE__, __FILE__, \"service descriptor not found\");\n"
        "        }\n"
        "        return ::datasystem::Status::OK();\n"
        "    }\n";
    printer.Print(vars, constructor.c_str());

    // Implement stub api declarations (same signatures as ZMQ path).
    ImplementStubApiDecl(printer, svc, indent);

    // Add ForgetRequest so GenericStub can delegate to it.
    printer.PrintRaw("    void ForgetRequest(int64_t tagId) {\n"
                     "        asyncCtx_.ForgetCall(tagId);\n"
                     "    }\n");

    // P5 7.3: per-stub liveness probe used by GenericStub delegation.
    printer.PrintRaw("    bool IsPeerAlive(uint32_t threshold) {\n"
                     "        (void)threshold;\n"
                     "        return channel_ != nullptr;\n"
                     "    }\n");

    printer.PrintRaw("private:\n    brpc::Channel *channel_;\n    int32_t timeoutMs_;\n"
                     "    const ::google::protobuf::ServiceDescriptor *svcDesc_;\n"
                     "    ::datasystem::BrpcAsyncContext asyncCtx_;\n};\n");
}

void RpcGenerator::GenerateBrpcGenericStubClass(io::Printer &printer,
                                                const google::protobuf::ServiceDescriptor &svc,
                                                const std::string &indent) const
{
    (void)indent;
    const std::string &svcName = svc.name();
    std::map<std::string, std::string> vars;
    vars["svc_name"] = svcName;
    vars["stub"] = svcName + "_BrpcGenericStub";

    const std::string constructor =
        "class $stub$ : public RpcStubBase {\n"
        "public:\n"
        "    explicit $stub$(brpc::Channel *channel, int32_t timeoutMs = -1);\n"
        "    ~$stub$() = default;\n"
        "    Status GetInitStatus() override;\n";
    printer.Print(vars, constructor.c_str());

    GenerateSvcName(printer, svcName, indent, false);

    ImplementStubApiDecl(printer, svc, indent);

    ImplementGenericStubOtherFuncDecl(printer);

    const std::string impl =
        "private:\n"
        "    std::unique_ptr<$svc_name$_BrpcStub> stub_;\n"
        "};\n";
    printer.Print(vars, impl.c_str());
}

// ============================================================================
// Brpc client stub cpp
// ============================================================================

void RpcGenerator::CreateBrpcStubCpp(const google::protobuf::FileDescriptor &file,
                                     compiler::GeneratorContext *generatorCtx) const
{
    std::unique_ptr<io::ZeroCopyOutputStream> outputFile(generatorCtx->Open(fileName + ".brpc.stub.pb.cc"));
    io::Printer printer(outputFile.get(), '$');

    GenerateBrpcStubCppPrologue(printer, file);
    printer.PrintRaw(namespaceBegin);

    for (auto i = 0; i < file.service_count(); ++i) {
        auto *svc = file.service(i);
        const std::string &svcName = svc->name();
        const std::string stub = svcName + "_BrpcStub";
        ImplementBrpcGenericStubApiDef(printer, *svc, PREFIX);
        ImplementBrpcStubApiDef(printer, *svc, PREFIX);
    }

    printer.PrintRaw(namespaceEnd);
}

void RpcGenerator::GenerateBrpcStubCppPrologue(io::Printer &printer,
                                               const google::protobuf::FileDescriptor &file) const
{
    std::map<std::string, std::string> vars;
    vars["full_file_name"] = file.name();
    vars["file_name"] = fileName;

    std::string impl =
        "// Generated by the protocol buffer compiler.  DO NOT EDIT!\n"
        "// source: $full_file_name$\n"
        "#include \"$file_name$.brpc.stub.pb.h\"\n"
        "#include <brpc/controller.h>\n";
    printer.Print(vars, impl.c_str());
}

// ============================================================================
// Generic stub implementation (delegates to inner BrpcStub)
// ============================================================================

void RpcGenerator::ImplementBrpcGenericStubApiDef(io::Printer &printer,
                                                  const google::protobuf::ServiceDescriptor &svc,
                                                  const std::string &indent)
{
    (void)indent;
    const std::string &svcName = svc.name();
    ImplementBrpcGenericStubConstructor(printer, svc, indent);
    const std::string stub = svcName + "_BrpcGenericStub";
    ImplementBrpcGenericStubOtherFuncDef(printer, stub);
    for (auto j = 0; j < svc.method_count(); ++j) {
        if (svc.method(j) == nullptr) {
            continue;
        }
        auto &method = *(svc.method(j));
        if (method.client_streaming() && method.server_streaming()) {
            ImplementBrpcGenericStubStreamingDef(printer, method, indent, stub);
        } else if (method.client_streaming() && !method.server_streaming()) {
            ImplementBrpcGenericStubClientStreamingDef(printer, method, indent, stub);
        } else if (!method.client_streaming() && method.server_streaming()) {
            ImplementBrpcGenericStubServerStreamingDef(printer, method, indent, stub);
        } else {
            ImplementBrpcGenericStubAsyncWriteDef(printer, method, indent, stub);
            ImplementBrpcGenericStubAsyncReadDef(printer, method, indent, stub);
            ImplementBrpcGenericStubNoStreamDef(printer, method, indent, stub);
            ImplementBrpcGenericStubNoStreamDef2(printer, method, indent, stub);
            ImplBrpcGenericStubNoStreamShortDef(printer, method, indent, stub);
        }
    }
}

void RpcGenerator::ImplementBrpcGenericStubConstructor(io::Printer &printer,
                                                       const google::protobuf::ServiceDescriptor &svc,
                                                       const std::string &indent)
{
    (void)indent;
    const std::string &svcName = svc.name();
    const std::string stub = svcName + "_BrpcGenericStub";
    std::map<std::string, std::string> vars;
    vars["stub"] = stub;
    vars["svcName"] = svcName;
    std::string impl =
        "$stub$::$stub$(brpc::Channel *channel, int32_t timeoutMs) {\n"
        "    stub_ = std::make_unique<$svcName$_BrpcStub>(channel, timeoutMs);\n"
        "}\n";
    printer.Print(vars, impl.c_str());
}

void RpcGenerator::ImplementBrpcGenericStubOtherFuncDef(io::Printer &printer, const std::string &stub)
{
    std::map<std::string, std::string> vars;
    vars["stub"] = stub;
    const std::string forgetRequest =
        "void $stub$::ForgetRequest(int64_t tagId) {\n"
        "    stub_->ForgetRequest(tagId);\n"
        "}\n";
    const std::string isPeerAlive =
        "bool $stub$::IsPeerAlive(uint32_t threshold) {\n"
        "    (void)threshold;\n"
        "    return stub_->IsPeerAlive(threshold);\n"
        "}\n";
    const std::string cacheSession =
        "void $stub$::CacheSession(bool cache) {\n"
        "    (void)cache;\n"
        "    // brpc channels are persistent, no-op\n"
        "}\n";
    const std::string getInitStatus =
        "Status $stub$::GetInitStatus() {\n"
        "    return stub_->GetInitStatus();\n"
        "}\n";
    const std::string setExclusiveConnInfo =
        "void $stub$::SetExclusiveConnInfo(const std::optional<int32_t> &exclusiveId,\n"
        "                                   const std::string &sockPath) {\n"
        "    (void)exclusiveId;\n"
        "    (void)sockPath;\n"
        "    // brpc does not support exclusive connections, no-op\n"
        "}\n";
    printer.Print(vars, forgetRequest.c_str());
    printer.Print(vars, isPeerAlive.c_str());
    printer.Print(vars, cacheSession.c_str());
    printer.Print(vars, getInitStatus.c_str());
    printer.Print(vars, setExclusiveConnInfo.c_str());
}

