std::string ZmqRpcGenerator::BuildSendRecvPayloadImpl()
{
    return
        "$indent$auto* req = dynamic_cast<const $inputTypeName$*>(request);\n"
        "$indent$auto* rsp = dynamic_cast<$outputTypeName$*>(response);\n"
        "$indent$if (req == nullptr || rsp == nullptr) {\n"
        "$indent$    cntl->SetFailed(\"Request/response type mismatch for $methodName$\");\n"
        "$indent$    return;\n"
        "$indent$}\n"
        "$indent$std::vector<::datasystem::RpcMessage> payload;\n"
        "$indent$std::vector<::datasystem::RpcMessage> outPayload;\n"
        "$indent$// Deserialize request_attachment -> payload\n"
        "$indent$butil::IOBuf &reqBuf = cntl->request_attachment();\n"
        "$indent$do {\n"
        "$indent$    if (reqBuf.empty()) { break; }\n"
        "$indent$    int64_t count = 0;\n"
        "$indent$    if (reqBuf.copy_to(reinterpret_cast<void*>(&count), sizeof(count)) != sizeof(count)) { break; }\n"
        "$indent$    reqBuf.pop_front(sizeof(count));\n"
        "$indent$    payload.resize(static_cast<size_t>(count));\n"
        "$indent$    for (auto& msg : payload) {\n"
        "$indent$        int64_t sz = 0;\n"
        "$indent$        if (reqBuf.copy_to(reinterpret_cast<void*>(&sz), sizeof(sz)) != sizeof(sz)) { break; }\n"
        "$indent$        reqBuf.pop_front(sizeof(sz));\n"
        "$indent$        if (msg.Resize(static_cast<size_t>(sz)).IsError()) { break; }\n"
        "$indent$        if (reqBuf.copy_to(msg.Data(), static_cast<size_t>(sz)) != static_cast<size_t>(sz)) "
        "{ break; }\n"
        "$indent$        reqBuf.pop_front(static_cast<size_t>(sz));\n"
        "$indent$    }\n"
        "$indent$} while (false);\n"
        "$indent$::datasystem::Status st = impl_.$methodName$(*req, *rsp, std::move(payload), outPayload);\n"
        "$indent$if (st.IsError()) {\n"
        "$indent$    cntl->SetFailed(st.GetMsg() + \"\\x01DS_ERR:\" + "
        "std::to_string(static_cast<int>(st.GetCode())) + \"\\x02\");\n"
        "$indent$} else {\n"
        "$indent$    // Serialize outPayload -> response_attachment\n"
        "$indent$    auto& rspBuf = cntl->response_attachment();\n"
        "$indent$    int64_t count = static_cast<int64_t>(outPayload.size());\n"
        "$indent$    rspBuf.append(reinterpret_cast<const void*>(&count), sizeof(count));\n"
        "$indent$    for (auto& msg : outPayload) {\n"
        "$indent$        int64_t sz = static_cast<int64_t>(msg.Size());\n"
        "$indent$        rspBuf.append(reinterpret_cast<const void*>(&sz), sizeof(sz));\n"
        "$indent$        rspBuf.append(msg.Data(), msg.Size());\n"
        "$indent$    }\n"
        "$indent$}\n";
}

// --- Pattern 4: unary_socket ---

void ZmqRpcGenerator::ImplementBrpcCallMethodUnarySocket(io::Printer &printer,
                                                         const google::protobuf::MethodDescriptor &method,
                                                         int methodIndex,
                                                         const std::string &indent)
{
    (void)indent;
    (void)methodIndex;
    std::map<std::string, std::string> vars;
    vars["indent"] = indent;
    vars["methodName"] = method.name();
    vars["inputTypeName"] = method.input_type()->name();
    vars["outputTypeName"] = method.output_type()->name();

    std::string impl =
        "$indent$// UnarySocket pattern: async completion via done closure.\n"
        "$indent$// Release done from ClosureGuard so serverApi owns it.\n"
        "$indent$google::protobuf::Closure* asyncDone = done_guard.release();\n"
        "$indent$auto serverApi =\n"
        "$indent$    std::make_shared<::datasystem::BrpcServerUnaryWriterReader<$outputTypeName$, $inputTypeName$>>(\n"
        "$indent$        cntl, request, response, asyncDone);\n";
    if (HasPayloadRecvOption(method)) {
        impl +=
        "$indent$// Defer done_->Run() so SendPayload()/SendAndTagPayload() can write\n"
        "$indent$// payload to response_attachment before brpc serializes the response.\n"
        "$indent$serverApi->SetHasRecvPayload(true);\n";
    }
    impl += BuildScTimeoutDurationInitSnippet();
    impl +=
        "$indent$::datasystem::Status st = impl_.$methodName$(serverApi);\n"
        "$indent$if (st.IsError()) {\n"
        "$indent$    cntl->SetFailed(st.GetMsg() + \"\\x01DS_ERR:\" + "
        "std::to_string(static_cast<int>(st.GetCode())) + \"\\x02\");\n"
        "$indent$    // Mark done as consumed so ~BrpcServerUnaryWriterReader does not double-close.\n"
        "$indent$    serverApi->MarkDoneConsumed();\n"
        "$indent$    // Run done to send the error response.\n"
        "$indent$    if (asyncDone) { asyncDone->Run(); }\n"
        "$indent$}\n"
        "$indent$// If st.IsOk(), done->Run() will be called by Write()/SendStatus()\n"
        "$indent$// (or by SendPayload()/SendAndTagPayload() when SetHasRecvPayload(true)).\n"
        "$indent$// Falls back to ~BrpcServerUnaryWriterReader() if none are called.\n";
    printer.Print(vars, impl.c_str());
}

std::string ZmqRpcGenerator::BuildScTimeoutDurationInitSnippet()
{
    return
        "$indent$// Initialize scTimeoutDuration from brpc deadline, matching ZMQ behavior.\n"
        "$indent$// NOTE (F07): scTimeoutDuration is thread_local. Under brpc bthread M:N\n"
        "$indent$// scheduling, the bthread handling this RPC may migrate to a different\n"
        "$indent$// pthread across yield points (e.g. master->worker sub-RPCs), so reads of\n"
        "$indent$// scTimeoutDuration from service impls after such yields may land on a\n"
        "$indent$// different pthread-local copy. Downstream service impls that depend on\n"
        "$indent$// scTimeoutDuration.CalcRealRemainingTime() after yields must re-Init() it\n"
        "$indent$// themselves (as master_sc_service_impl.cpp already does). A full fix\n"
        "$indent$// requires moving scTimeoutDuration off thread_local (bthread-local or\n"
        "$indent$// Controller-carried); tracked as follow-up, not done here to avoid\n"
        "$indent$// touching all service files.\n"
        "$indent${\n"
        "$indent$    int64_t deadlineUs = cntl->deadline_us();\n"
        "$indent$    if (deadlineUs > 0) {\n"
        "$indent$        struct timeval tv;\n"
        "$indent$        gettimeofday(&tv, nullptr);\n"
        "$indent$        int64_t nowUs = static_cast<int64_t>(tv.tv_sec) * 1000000L + tv.tv_usec;\n"
        "$indent$        // Round up (F17): integer truncation would turn (0, 999us] into 0ms,\n"
        "$indent$        // causing scTimeoutDuration.Init() with default (infinite) timeout.\n"
        "$indent$        int64_t remainingMs = (deadlineUs - nowUs + 999) / 1000;\n"
        "$indent$        if (remainingMs > 0) {\n"
        "$indent$            scTimeoutDuration.Init(remainingMs);\n"
        "$indent$        } else {\n"
        "$indent$            scTimeoutDuration.Init();\n"
        "$indent$        }\n"
        "$indent$    } else {\n"
        "$indent$        scTimeoutDuration.Init();\n"
        "$indent$    }\n"
        "$indent$}\n";
}

